<template>
  <div class="login-container">
    <el-form
      ref="adminForm"
      :model="adminSignInForm"
      :rules="loginRules"
      class="login-form"
      autocomplete="on"
      label-position="left"
    >
      <div class="title-container">
        <h3 class="title">
          {{ $t('login.title') }}
        </h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon name="user" />
        </span>
        <el-input
          ref="username"
          v-model="adminSignInForm.username"
          name="username"
          type="text"
          autocomplete="on"
          maxlength="20"
          :placeholder="$t('placeholder.username')"
        />
      </el-form-item>

      <el-tooltip
        v-model="capsTooltip"
        content="Caps lock is On"
        placement="right"
        manual
      >
        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon name="password" />
          </span>
          <el-input
            :key="passwordType"
            ref="password"
            v-model="adminSignInForm.password"
            :type="passwordType"
            :placeholder="$t('placeholder.password')"
            name="password"
            tabindex="2"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
            @keyup.enter.native="handleLogin"
          />
          <span
            class="show-pwd"
            @click="showPwd"
          >
            <svg-icon :name="passwordType === 'password' ? 'eye-off' : 'eye-on'" />
          </span>
        </el-form-item>
      </el-tooltip>

      <el-button
        :loading="loading"
        type="primary"
        style="width:100%; margin-bottom:30px;"
        @click.native.prevent="handleLogin"
      >
        {{ $t('login.logIn') }}
      </el-button>

      <div style="position:relative">
        <div class="tips">
          <span>{{ $t('login.username') }} : admin (winikim-server-starter-kit 설정 값)</span> <br/>
          <span>{{ $t('login.password') }} : winikim!@ (winikim-server-starter-kit 설정 값)</span>
        </div>
      </div>
    </el-form>

    <el-dialog
      :title="$t('login.thirdparty')"
      :visible.sync="showDialog"
    >
      {{ $t('login.thirdpartyTips') }}
      <br>
      <br>
      <br>
      <social-sign />
    </el-dialog>
  </div>
</template>

<script lang="ts">
import { Component, Vue, Watch } from 'vue-property-decorator'
import { Route } from 'vue-router'
import { Dictionary } from 'vue-router/types/router'
import { Form as ElForm, Input } from 'element-ui'
import { AdminSignIn } from '@/api/type/sign/sign-in'
import { AdminUserModule } from '@/store/modules/admin-user'
import { ElFormRule } from '@/utils/el-rule'

@Component({
  name: 'AdminLoginUI'
})
export default class AdminLoginUI extends Vue {
  public activeName = 'first';
  private elFormRule = new ElFormRule()

  private loginRules = {
    username: [{ required: true, validator: this.elFormRule.validateEmptyUsername, trigger: 'blur' }],
    password: [{ required: true, validator: this.elFormRule.validatePassword, trigger: 'blur' }]
  };

  private passwordType = 'password';
  private loading = false;
  private showDialog = false;
  private capsTooltip = false;
  private redirect?: string;
  private otherQuery: Dictionary<string> = {};
  private adminSignInForm = {
    username: '',
    password: ''
  }

  public mounted() {
    if (this.adminSignInForm.username === '') {
      (this.$refs.username as Input).focus()
    } else if (this.adminSignInForm.password === '') {
      (this.$refs.password as Input).focus()
    }
  }

  @Watch('$route', { immediate: true })
  private onRouteChange(route: Route) {
    // TODO: remove the "as Dictionary<string>" hack after v4 release for vue-router
    // See https://github.com/vuejs/vue-router/pull/2050 for details
    const query = route.query as Dictionary<string>
    if (query) {
      this.redirect = query.redirect
      this.otherQuery = this.getOtherQuery(query)
    }
  }

  private showPwd() {
    if (this.passwordType === 'password') {
      this.passwordType = ''
    } else {
      this.passwordType = 'password'
    }
    this.$nextTick(() => {
      (this.$refs.password as Input).focus()
    })
  }

  private handleLogin() {
    (this.$refs.adminForm as ElForm).validate(async(valid: boolean) => {
      if (valid) {
        this.loading = true
        const adminSignIn = new AdminSignIn()
        adminSignIn.$username = this.adminSignInForm.username
        adminSignIn.$password = this.adminSignInForm.password
        await AdminUserModule.Login(adminSignIn).then(() => {
          this.loading = false
          this.$router.push({
            path: this.redirect || '/',
            query: this.otherQuery
          })
        }).catch(() => {
          this.loading = false
          this.$notify.error({
            title: this.$t('error.name').toString(),
            message: this.$t('error.signIn').toString()
          })
        })
      } else {
        this.loading = false
        this.$notify.warning({
          title: this.$t('error.name').toString(),
          message: this.$t('error.form').toString()
        })
        return false
      }
    })
  }

  private checkCapslock(e: KeyboardEvent) {
    const { key } = e
    this.capsTooltip = key !== null && key.length === 1 && (key >= 'A' && key <= 'Z')
  }

  private getOtherQuery(query: Dictionary<string>) {
    return Object.keys(query).reduce((acc, cur) => {
      if (cur !== 'redirect') {
        acc[cur] = query[cur]
      }
      return acc
    }, {} as Dictionary<string>)
  }
}
</script>

